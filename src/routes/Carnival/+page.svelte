<h1>Carnival</h1>

<p>
	Returning to the 2025 Kitsap Fair and Rodeo, the best carnival in the Northwest! Funtastic Shows
	will be providing the carnival and games. You are familiar with their safety and quality standards
	if you have adventured to the Washington State Fair.
</p>

<p>
	All rides and games will be in one designated area so your family of all ages can enjoy the fun
	together.
</p>

<p>
	GOOD NEWS! Carnival wristbands will be back for this year's fair. Wristband day will be Sunday,
	Aug. 24. Wristbands will be $50 per individual and non-transferable. Ride as many rides as you'd
	like and play three (games) between 11:00am and 6:00pm with your wristband. These can be purchased
	at the carnival ticket booths on wristband day only - no pre-sales.
</p>

<p>
	Carnival Hours: Wednesday, 11:00am - 11:00pm Thursday, 11:00am - 11:00pm Friday, 11:00am - 11:00pm
	Saturday, 11:00am - 11:00pm Sunday, 11:00am - 7:00pm (WRISTBAND DAY until 6:00pm)
</p>
